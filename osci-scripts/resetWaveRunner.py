#!/usr/bin/env python3

import vxi11

# --------------------------------------------------------------------------- #
# DESCRIPTION                                                                 #
# --------------------------------------------------------------------------- #
# Resets the oscilloscope.

# --------------------------------------------------------------------------- #
# USAGE                                                                       #
# --------------------------------------------------------------------------- #
# ./resetWaveRunner.py

# --------------------------------------------------------------------------- #
# SETTINGS                                                                    #
# --------------------------------------------------------------------------- #
INSTR_IP='169.254.14.189'

# --------------------------------------------------------------------------- #
# IMPLEMENTATION                                                              #
# --------------------------------------------------------------------------- #
instr=vxi11.Instrument(INSTR_IP)
print(instr.ask('*IDN?'))
instr.write('*RST')
